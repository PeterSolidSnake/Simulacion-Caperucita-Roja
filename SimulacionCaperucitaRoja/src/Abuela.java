public class Abuela {

    private int edad;
    private boolean salud;

    public Abuela(int edad, boolean salud) {
        this.edad = edad;
        this.salud = salud;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public boolean isSalud() {
        return salud;
    }

    public void setSalud(boolean salud) {
        this.salud = salud;
    }
}
